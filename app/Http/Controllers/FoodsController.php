    public function search(Request $request){
        $foodsAll = Restaurant::whereHas('foods',function($query) use ($request){
            $query->where('food_item','like','%'.$request->q.'%');
        })->get();
        // echo($foodsAll);
        return view('restaurants.searchShow')->with(['foodsAll'=>$foodsAll]);
    }

